--- show products with brand, category, and pricing
CREATE OR REPLACE VIEW api.vw_product_full AS  
    SELECT 
        p.id product_id, p.label product_label, p.shortdescription product_shortdescription, p.longdescription product_longdescription, 
        pc.label category_label, pc.description category_description,
        pb.label brand_label, pb.description brand_description,
        pv.attributes product_variant_attributes,
        pvp.price, pvp.validity, pvp.current  
        FROM product p
        JOIN category pc ON p.category_id = pc.id
        JOIN brand pb ON p.brand_id = pb.id
        LEFT JOIN product_variant pv ON p.id = pv.product_id
        LEFT JOIN product_variant_price pvp ON pvp.product_variant_id = pv.id
        ORDER BY p.id, pvp.validity ASC;


--- focused view with a subset of columns
CREATE OR REPLACE VIEW api.vw_product_focus AS 
    SELECT product_id, product_label, product_shortdescription, category_label, brand_label, product_variant_attributes, price, validity, current
        FROM vw_product_full
        WHERE validity @> current_date;

