/*
    Miscellaneous API elements


*/
CREATE OR REPLACE PROCEDURE api.update_current_price_flags()
    LANGUAGE SQL
    AS $$
    UPDATE product.product_variant_price
    SET current = (validity @> current_date);
    $$;

--- identify any product variants without current prices
CREATE OR REPLACE VIEW api.vw_product_variant_without_current_price AS 
    SELECT 
        pv.id AS product_variant_id, pv.attributes AS product_variant_attributes, 
        p.id as product_id, p.label AS product_label 
    FROM product_variant pv 
    JOIN product p ON p.id = pv.product_id
    WHERE NOT EXISTS 
        (SELECT 1 FROM product_variant_price pvp 
            WHERE pvp.current = TRUE 
                AND pvp.product_variant_id = pv.id);


